package com.example.gastospessoais.Modelo;

public class Valores {

    private Float receita;
    private Float gastos;
    private Float disponivel;




    public Float getReceita() {
        return receita;
    }

    public void setReceita(Float receita) {
        this.receita = receita;
    }

    public Float getGastos() {
        return gastos;
    }

    public void setGastos(Float gastos) {
        this.gastos = gastos;
    }

    public Float getDisponivel() {
        return disponivel;
    }

    public void setDisponivel(Float disponivel) {
        this.disponivel = disponivel;
    }
}
